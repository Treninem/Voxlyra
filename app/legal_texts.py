from __future__ import annotations

from dataclasses import dataclass
from hashlib import sha256
from html import escape
import re

from app.config import settings


LEGAL_VERSION = "2026-07-10"
REFUNDS_VERSION = "2026-07-11"


@dataclass(frozen=True)
class LegalDoc:
    code: str
    title: str
    short_title: str
    version: str
    body: str
    audience: str = "all"
    consent_kind: str = "agreement"
    filename: str = "document.pdf"

    @property
    def plain_text(self) -> str:
        text = re.sub(r"<br\s*/?>", "\n", self.body, flags=re.I)
        text = re.sub(r"</p\s*>", "\n\n", text, flags=re.I)
        text = re.sub(r"</?(?:b|strong|i|em|u|p|div|span)[^>]*>", "", text, flags=re.I)
        text = re.sub(r"<[^>]+>", "", text)
        text = text.replace("&nbsp;", " ").replace("&amp;", "&").replace("&lt;", "<").replace("&gt;", ">")
        text = re.sub(r"\n{3,}", "\n\n", text)
        return text.strip()

    @property
    def digest(self) -> str:
        payload = f"{self.code}\n{self.version}\n{self.plain_text}".encode("utf-8")
        return sha256(payload).hexdigest()


def operator_is_configured() -> bool:
    required = (
        settings.LEGAL_OPERATOR_NAME,
        settings.LEGAL_OPERATOR_STATUS,
        settings.LEGAL_OPERATOR_INN,
        settings.LEGAL_OPERATOR_OGRN,
        settings.LEGAL_OPERATOR_ADDRESS,
        settings.LEGAL_CONTACT_EMAIL,
    )
    return all(str(value or "").strip() for value in required)


def operator_block() -> str:
    name = escape(settings.LEGAL_OPERATOR_NAME.strip() or "[наименование владельца платформы]")
    status = escape(settings.LEGAL_OPERATOR_STATUS.strip() or "[ИП/ООО/иной статус]")
    inn = escape(settings.LEGAL_OPERATOR_INN.strip() or "[ИНН]")
    ogrn = escape(settings.LEGAL_OPERATOR_OGRN.strip() or "[ОГРН/ОГРНИП]")
    address = escape(settings.LEGAL_OPERATOR_ADDRESS.strip() or "[юридический/почтовый адрес]")
    email = escape(settings.LEGAL_CONTACT_EMAIL.strip() or "[электронная почта]")
    support = escape(settings.LEGAL_SUPPORT_CONTACT.strip() or "поддержка внутри Вокслиры")
    return (
        f"Оператор платформы: <b>{name}</b>, {status}; ИНН {inn}; ОГРН/ОГРНИП {ogrn}; "
        f"адрес: {address}; электронная почта: {email}; канал обращений: {support}."
    )


def _header(title: str, version: str = LEGAL_VERSION) -> str:
    draft = "\n\n<b>Внимание:</b> документ является шаблоном до заполнения реквизитов оператора." if not operator_is_configured() else ""
    return f"<b>{title}</b>\n\nРедакция: {version}\n\n{operator_block()}{draft}\n\n"


