def get_doc(code: str) -> LegalDoc | None:
    return LEGAL_DOCS.get(code)


def all_docs() -> list[LegalDoc]:
    seen: set[tuple[str, str]] = set()
    result: list[LegalDoc] = []
    for doc in LEGAL_DOCS.values():
        key = (doc.code, doc.version)
        if key in seen:
            continue
        seen.add(key)
        result.append(doc)
    return result


def public_doc_url(code: str, pdf: bool = False) -> str:
    base = (settings.LEGAL_DOCS_BASE_URL or settings.WEBAPP_URL).strip().rstrip("/")
    if not base:
        return ""
    suffix = ".pdf" if pdf else ""
    return f"{base}/legal/{code}{suffix}"
