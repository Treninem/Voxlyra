@dataclass(frozen=True)
class LegalDoc:
    code: str
    title: str
    short_title: str
    version: str
    body: str
    audience: str = "all"
    consent_kind: str = "agreement"
    filename: str = "document.pdf"

    @property
    def plain_text(self) -> str:
        text = re.sub(r"<br\s*/?>", "\n", self.body, flags=re.I)
        text = re.sub(r"</p\s*>", "\n\n", text, flags=re.I)
        text = re.sub(r"</?(?:b|strong|i|em|u|p|div|span)[^>]*>", "", text, flags=re.I)
        text = re.sub(r"<[^>]+>", "", text)
        text = text.replace("&nbsp;", " ").replace("&amp;", "&").replace("&lt;", "<").replace("&gt;", ">")
        text = re.sub(r"\n{3,}", "\n\n", text)
        return text.strip()

    @property
    def digest(self) -> str:
        payload = f"{self.code}\n{self.version}\n{self.plain_text}".encode("utf-8")
        return sha256(payload).hexdigest()


